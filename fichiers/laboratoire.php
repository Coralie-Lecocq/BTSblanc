        <h2>Le laboratoire</h2>
        <h3>Un suivi personnalisé</h3>
        <p>Tous nos patients bénéficient d'un suivi personnalisé, en fonction des besoins de chacun. À l'écoute des patients, nous nous adaptons au cas par cas.</p>

        <h3>Dépistage</h3>
        <p>Au sein même de notre laboratoire, nous effectuons les dépistages des maladies virales dont le VIH et les les hépatites. Ces tests sont gratuits et les résultats leur sont communiqués dans la demi-journée suivant la prise de sang.</p>

        <h3>Recherche</h3>
        <p>Notre médecin chercheur participe activement à la découverte de nouveaux traitements pour les maladies virales dont nous sommes spécialisés.</p>

        <h3>Nouvelles technologies</h3>
        <p>Quant aux nouvelles technologies, nous disposons des dernières nouveautés en termes de matériel médical et de techniques d'utilisation.</p><br/>
